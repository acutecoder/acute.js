	var gateway = {
	
		ajax : function ( json, call_back, data_type )
		{
			this.data_type = data_type ? data_type : 'json';
			
			$.ajax({
				type	: 'GET',
				data	: json,
				dataType: this.data_type
			}).done( function( data ){
				if( call_back !== undefined )
				{
					call_back( data );
				}	
			} );
		}
	}
	
	
////////////////////////////////////////////////////////////////////////////////////////////////////
	$.ajaxSetup({	url: 'gateway.php'	});		////////////////////////////////////////////////////
